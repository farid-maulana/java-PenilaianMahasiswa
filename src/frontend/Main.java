    public void dataKelas() {
        String[] kolom = {"ID", "Nama Kelas", "Tingkat"};
        ArrayList<Kelas> list = new Kelas().getAll();
        Object rowData[] = new Object[3];
        
        tblKelas.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Kelas kelas : list) {
            rowData[0] = kelas.getidKelas();
            rowData[1] = kelas.getNamaKelas();
            rowData[2] = kelas.getTingkat();
            
            ((DefaultTableModel) tblKelas.getModel()).addRow(rowData);
        }
    }
    
